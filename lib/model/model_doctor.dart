class Doctor {
  final String name;
  final String age;
  final String specialty;
  final String phone;
  final String address;
  final String imageUrl;
  final String email;

  Doctor({
    required this.email,
    required this.phone,
    required this.name,
    required this.specialty,
    required this.imageUrl,
    required this.age,
    required this.address,
  });
}
